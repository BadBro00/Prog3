/*
                                            |Approcci alla programmazione OO|
---------------------------------------------------------------------------------------------------------------------
La classe contenuta nel file dev'essere public

Il metodo main, non agendo su un oggetto, e' istanziato come static

Gli argomenti passati da riga di comando al main sono dichiarati come String args[]

Per stampare a video, si usa il comando System.out.println(...):
      i) System -> Classe System
      ii) out -> Oggetto della classe System
      iii) println() -> metodo dell'oggetto out
*/

public class Lezione2{
  public static void main(String args[]){
    System.out.println("Hello World!");
  }
}
